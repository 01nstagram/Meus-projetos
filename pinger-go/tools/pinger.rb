def pingar_ate_explodir
  print corzinha("\n[?] Digita o IP ou dominio do infeliz: ", :ciano)
  alvo = gets.chomp.strip

  if alvo.empty?
    puts corzinha("\n[-] Digita essa merda direito jumento\n", :vermelho)
    return
  end

  puts corzinha("\n[~] Iniciando o ataque  #{alvo}...\n", :rosa)

  loop do
    output = `ping -c 1 #{alvo} 2>&1`

    if output.include?("bytes from") || output.include?("ttl=")
      puts corzinha("[+] LIVE - #{alvo} [#{Time.now}]", :verde)
    elsif output.include?("Name or service not known") || output.include?("unknown host")
      puts corzinha("[-] Tu digitou merda - IP ou dominio invalido", :vermelho)
      break
    else
      puts corzinha("[X] X_X - #{alvo} [#{Time.now}]", :vermelho)
      break
    end

    sleep 1
  end
end

